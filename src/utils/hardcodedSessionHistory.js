const HARD_CODED_ATTENDANCE_RECORDS_IT4R10 = HARD_CODED_SESSIONS_IT4R10.flatMap(
  session => {
    const baseStart =
      (session.startTime && new Date(session.startTime)) ||
      new Date(`${session.sessionDate}T08:00:00+08:00`)

    const sessionDateIso = new Date(session.sessionDate).toISOString()

    return session.participants.map((p, index) => {
      const safeDuration =
        typeof p.durationSeconds === 'number' &&
        !Number.isNaN(p.durationSeconds)
          ? Math.max(0, Math.floor(p.durationSeconds))
          : 0

      // Stagger join times slightly per participant to avoid identical timestamps
      const joinDate = new Date(baseStart.getTime() + index * 1000)
      const leaveDate = new Date(joinDate.getTime() + safeDuration * 1000)

      const joinTime = joinDate.toTimeString().slice(0, 8) // HH:MM:SS
      const leaveTime = leaveDate.toTimeString().slice(0, 8)

      const status = p.status || 'Absent'
      const rawStatus = status.toLowerCase()

      const user = {
        _id: `hc-user-${p.studentId}`,
        firstName: p.name,
        lastName: '',
        studentId: p.studentId,
        email: '',
        section: session.section || 'IT4R10',
        schoolYear: '2025-2026',
        semester: '1st Semester',
        role: 'student'
      }

      return {
        attendanceId: `hc-${session.id}-${p.studentId}`,
        userId: user._id,
        studentId: p.studentId,
        participantName: p.name,
        fullName: p.name,
        meetCode: session.meetCode,
        subjectId: session.subjectId,
        subjectName: session.subjectName,
        groupName: session.subjectName,
        sessionDate: sessionDateIso,
        joinTime,
        joinTimeIso: joinDate.toISOString(),
        leaveTime,
        leaveTimeIso: leaveDate.toISOString(),
        durationSeconds: safeDuration,
        duration: safeDuration,
        status,
        rawStatus,
        isTardy: false,
        tardinessCount: 0,
        createdAt: joinDate.toISOString(),
        updatedAt: leaveDate.toISOString(),
        instructorId: null,
        isInstructorRecord: false,
        isUnauthenticated: false,
        authWarning: false,
        user
      }
    })
  }
)

